		private ScreenSpaceAmbientObscurance ssaoComponent = null;

		public SSAOEffect(EffectConfig config)
		{
			ssaoComponent = Camera.main.gameObject.GetComponent<ScreenSpaceAmbientObscurance>();

			applyConfig(config);
		}

		~SSAOEffect()
		{
			Disable();
		}
		
		public void drawGUI(EffectConfig config, float x, float y)
		{
			config.ssaoEnabled = GUI.Toggle(new Rect(x, y, 200, 20), config.ssaoEnabled, "enabled");
			y += 25;

			config.ssaoIntensity = DrawGUI.drawSliderWithLabel(x, y, 0.0f, 3.0f, "ssaoIntensity", config.ssaoIntensity);
			y += 25;

			config.ssaoRadius = DrawGUI.drawSliderWithLabel(x, y, 0.0f, 5.0f, "ssaoRadius", config.ssaoRadius);
			y += 25;

			config.ssaoBlurFilterDistance = DrawGUI.drawSliderWithLabel(x, y, 0.0f, 5.0f, "ssaoBlurFilterDistance", config.ssaoBlurFilterDistance);
			y += 25;

			config.ssaoBlurFilterIterations = DrawGUI.drawIntSliderWithLabel(x, y, 0, 3, "ssaoBlurFilterIterations", config.ssaoBlurFilterIterations);
			y += 25;

			config.ssaoDownsample = DrawGUI.drawIntSliderWithLabel(x, y, 0, 1, "ssaoDownsample", config.ssaoDownsample);
			y += 25;

			applyConfig(config);
		}

		private void applyConfig(EffectConfig config)
		{
			if (config.ssaoEnabled)
			{
				Enable();

				ssaoComponent.blurIterations = config.ssaoBlurFilterIterations;
				ssaoComponent.blurFilterDistance = config.ssaoBlurFilterDistance;
				ssaoComponent.intensity = config.ssaoIntensity;
				ssaoComponent.radius = config.ssaoRadius;
				ssaoComponent.downsample = config.ssaoDownsample;
			}
			else
			{
				Disable();
			}
		}
		
		private void Enable()
		{
			if (ssaoComponent == null)
			{
				ssaoComponent = Camera.main.gameObject.AddComponent<ScreenSpaceAmbientObscurance>();
				if (ssaoComponent == null)
				{
					Debug.LogError("MotionblurEffect: Could not add component CameraMotionBlur to Camera.");
				}
				else
				{
					Material ssaoMaterial = new Material(ssaoShaderText);
					Texture2D randTexture = new Texture2D(64, 64);
					randTexture.LoadImage(ssaoRandomTexture);

					ssaoComponent.aoShader = ssaoMaterial.shader;
					ssaoComponent.rand = randTexture;
				}
			}
			
			ssaoComponent.enabled = true;
		}

		private void Disable()
		{
			if (ssaoComponent != null)
			{
				MonoBehaviour.Destroy(ssaoComponent);
				ssaoComponent = null;
			}
		}

